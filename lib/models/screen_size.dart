
enum ScreenSize {
  /// Screen is at least approximately 320x426 dp units, corresponds to the small resource qualifier.
  small,
  /// Screen is at least approximately 320x470 dp units, corresponds to the normal resource qualifier.
  normal,
  /// Screen is at least approximately 480x640 dp units, corresponds to the large resource qualifier.
  large,
  /// Screen is at least approximately 720x960 dp units, corresponds to the xlarge resource qualifier.
  xlarge
}